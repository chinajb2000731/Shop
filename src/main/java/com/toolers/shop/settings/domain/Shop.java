package com.toolers.shop.settings.domain;

public class Shop {

    private String id;
    private String sid;
    private String shopname;
    private String shopaddress;
    private String shopdesc;
    private String selleridcard;
    private String idcardphoto;
    private String businessscope;
    private String pimage;
    private String shopflag;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getShopname() {
        return shopname;
    }

    public void setShopname(String shopname) {
        this.shopname = shopname;
    }

    public String getShopaddress() {
        return shopaddress;
    }

    public void setShopaddress(String shopaddress) {
        this.shopaddress = shopaddress;
    }

    public String getShopdesc() {
        return shopdesc;
    }

    public void setShopdesc(String shopdesc) {
        this.shopdesc = shopdesc;
    }

    public String getSelleridcard() {
        return selleridcard;
    }

    public void setSelleridcard(String selleridcard) {
        this.selleridcard = selleridcard;
    }

    public String getIdcardphoto() {
        return idcardphoto;
    }

    public void setIdcardphoto(String idcardphoto) {
        this.idcardphoto = idcardphoto;
    }

    public String getBusinessscope() {
        return businessscope;
    }

    public void setBusinessscope(String businessscope) {
        this.businessscope = businessscope;
    }

    public String getPimage() {
        return pimage;
    }

    public void setPimage(String pimage) {
        this.pimage = pimage;
    }

    public String getShopflag() {
        return shopflag;
    }

    public void setShopflag(String shopflag) {
        this.shopflag = shopflag;
    }
}

